/*
7) Use While loop to generate random numbers and maintain a running sum of these values.
   Terminate when the sum exceeds 20.
   (Note : use Math.random() method to obtain numbers )
*/
package pack;

public class Seven {

  public static void main(String[] args) {
    double sum = 0;
    while (sum <= 20) {
      double rd_num = Math.random();
      System.out.println(rd_num);
      sum += rd_num;
    }
    System.out.println("sum =>> " + sum);
  }
}
